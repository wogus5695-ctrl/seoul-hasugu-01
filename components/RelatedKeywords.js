export default function RelatedKeywords({ keyword, hasKeyword }) {
  // 기본 키워드가 없을 경우 추천용 기본 목록 제공
  if (!hasKeyword || !keyword) {
    const defaultKeywords = [
      { name: '일산 하수구막힘', param: '일산-하수구막힘' },
      { name: '일산 싱크대막힘', param: '일산-싱크대막힘' },
      { name: '파주 하수구막힘', param: '파주-하수구막힘' },
      { name: '파주 싱크대막힘', param: '파주-싱크대막힘' },
      { name: '김포 하수구막힘', param: '김포-하수구막힘' },
      { name: '김포 싱크대막힘', param: '김포-싱크대막힘' }
    ];
    return renderLinks(defaultKeywords);
  }

  // 1. 키워드 분석 (예: '식사동 싱크대막힘' -> region: '식사동', job: '싱크대막힘')
  const parts = keyword.trim().split(' ');
  const region = parts[0] || '일산';
  const job = parts[1] || '하수구막힘';

  // 2. 관련 작업명 4개 추출 (현재 작업명 제외)
  const otherJobs = JOBS.filter((j) => j !== job).slice(0, 4);
  const sameRegionLinks = otherJobs.map((j) => ({
    name: `${region} ${j}`,
    param: `${region}-${j}`
  }));

  // 3. 관련 지역 3~4개 추출
  const relatedRegions = parentRegions[region] || ['일산', '파주', '김포'];
  const sameJobLinks = relatedRegions.map((r) => ({
    name: `${r} ${job}`,
    param: `${r}-${job}`
  }));

  // 4. 두 링크 리스트 합치기 (최대 8개 제한)
  const allLinks = [...sameRegionLinks, ...sameJobLinks].slice(0, 8);

  return renderLinks(allLinks);
}

function renderLinks(links) {
  return (
    <section className={styles.section}>
      <div className="container">
        <h3 className={styles.title}>함께 확인하는 배관 막힘 키워드</h3>
        <p className={styles.description}>
          현재 보고 있는 지역과 작업명에 가까운 키워드를 함께 확인할 수 있습니다.
        </p>
        <div className={styles.grid}>
          {links.map((link, index) => (
            <Link 
              key={index} 
              href={`/?k=${encodeURIComponent(link.param)}`} 
              className={styles.linkPill}
            >
              {link.name}
            </Link>
          ))}
        </div>
      </div>
    </section>
  );
}
